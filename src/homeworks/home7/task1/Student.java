package homeworks.home7.task1;

public class Student {
    private String name;

    private String groupStud;

    public Student() {
    }

    public Student(String name, String groupStud) {
        this.name = name;
        this.groupStud = groupStud;
    }

    public String getName() {
        return name;
    }

    public String getGroupStud() {
        return groupStud;
    }
}
